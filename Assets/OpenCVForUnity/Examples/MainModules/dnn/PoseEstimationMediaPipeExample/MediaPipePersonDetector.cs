        public MediaPipePersonDetector(string modelFilepath, float nmsThreshold = 0.3f, float scoreThreshold = 0.5f, int topK = 1, int backend = Dnn.DNN_BACKEND_OPENCV, int target = Dnn.DNN_TARGET_CPU)
        {
            // initialize
            if (!string.IsNullOrEmpty(modelFilepath))
            {
                person_detection_net = Dnn.readNet(modelFilepath);
            }

            nms_threshold = Mathf.Clamp01(nmsThreshold);
            score_threshold = Mathf.Clamp01(scoreThreshold);
            this.topK = topK;
            this.backend = backend;
            this.target = target;

            person_detection_net.setPreferableBackend(this.backend);
            person_detection_net.setPreferableTarget(this.target);

            anchors = load_anchors();
        }

        protected virtual Mat preprocess(Mat image)
        {
            // Add padding to make it square.
            int max = Mathf.Max(image.cols(), image.rows());

            if (maxSizeImg == null)
                maxSizeImg = new Mat(max, max, image.type(), Scalar.all(0));
            if (maxSizeImg.width() != max || maxSizeImg.height() != max)
            {
                maxSizeImg.create(max, max, image.type());
                Imgproc.rectangle(maxSizeImg, new OpenCVRect(0, 0, maxSizeImg.width(), maxSizeImg.height()), Scalar.all(0), -1);
            }

            Mat _maxSizeImg_roi = new Mat(maxSizeImg, new OpenCVRect((max - image.cols()) / 2, (max - image.rows()) / 2, image.cols(), image.rows()));
            image.copyTo(_maxSizeImg_roi);


            // Create a 4D blob from a frame.
            Mat blob = Dnn.blobFromImage(maxSizeImg, 1.0 / 255.0, input_size, Scalar.all(0), true, false, CvType.CV_32F); // HWC to NCHW, BGR to RGB

            int c = image.channels();
            int h = (int)input_size.height;
            int w = (int)input_size.width;

            Mat blob_cxhxw = blob.reshape(1, new int[] { c, h, w });// [c, h, w]

            for (int i = 0; i < c; ++i)
            {
                Mat blob_1xhw = blob_cxhxw.row(i).reshape(1, 1);// [1, h, w] => [1, h * w]

                // # [0, 1] -> [-1, 1]
                Core.subtract(blob_1xhw, new Scalar(0.5), blob_1xhw);
                Core.multiply(blob_1xhw, new Scalar(2.0), blob_1xhw);
            }

            return blob;// [1, 3, 224, 224]
        }

        public virtual Mat infer(Mat image)
        {
            // cheack
            if (image.channels() != 3)
            {
                Debug.Log("The input image must be in BGR format.");
                return new Mat();
            }

            // Preprocess
            Mat input_blob = preprocess(image);

            // Forward
            person_detection_net.setInput(input_blob);
            List<Mat> output_blob = new List<Mat>();
            person_detection_net.forward(output_blob, person_detection_net.getUnconnectedOutLayersNames());

            // Postprocess
            Mat results = postprocess(output_blob);

            // scale_boxes
            float maxSize = Mathf.Max((float)image.size().width, (float)image.size().height);
            float x_factor = maxSize;
            float y_factor = maxSize;
            float x_shift = (maxSize - (float)image.size().width) / 2f;
            float y_shift = (maxSize - (float)image.size().height) / 2f;

            for (int i = 0; i < results.rows(); ++i)
            {
                float[] results_arr = new float[4];
                results.get(i, 0, results_arr);
                float x1 = Mathf.Floor(results_arr[0] * x_factor - x_shift);
                float y1 = Mathf.Floor(results_arr[1] * y_factor - y_shift);
                float x2 = Mathf.Floor(results_arr[2] * x_factor - x_shift);
                float y2 = Mathf.Floor(results_arr[3] * y_factor - y_shift);

                results.put(i, 0, new float[] { x1, y1, x2, y2 });

                float[] landmarks_arr = new float[8];
                results.get(i, 4, landmarks_arr);
                for (int j = 0; j < 8; ++j)
                {
                    if (j % 2 == 0)
                    {
                        landmarks_arr[j] = Mathf.Floor(landmarks_arr[j] * x_factor - x_shift);
                    }
                    else
                    {
                        landmarks_arr[j] = Mathf.Floor(landmarks_arr[j] * y_factor - y_shift);
                    }
                }

                results.put(i, 4, landmarks_arr);
            }

            input_blob.Dispose();
            for (int i = 0; i < output_blob.Count; i++)
            {
                output_blob[i].Dispose();
            }

            return results;
        }

        protected virtual Mat postprocess(List<Mat> output_blob)
        {
            int num = output_blob[0].size(1);
            Mat output_blob_1_numx1 = output_blob[1].reshape(1, num);
            Mat score = output_blob_1_numx1.colRange(new OpenCVRange(0, 1));

            Mat output_blob_0_numx12 = output_blob[0].reshape(1, num);
            Mat box_delta = output_blob_0_numx12.colRange(new OpenCVRange(0, 4));
            Mat landmark_delta = output_blob_0_numx12.colRange(new OpenCVRange(4, 12));

            // get scores
            // # score = np.clip(score, -100, 100)
            numpy_clip(score, -100, 100);

            // # score = 1 / (1 + np.exp(-score))
            Core.multiply(score, Scalar.all(1.0), score, -1.0);
            Core.exp(score, score);
            Core.add(score, Scalar.all(1.0), score);
            Core.divide(1.0, score, score);

            // get boxes
            Mat cxy_delta = box_delta.colRange(new OpenCVRange(0, 2));
            Mat _cxy_delta_numx1_c2 = cxy_delta.reshape(2, cxy_delta.rows());
            Core.divide(_cxy_delta_numx1_c2, new Scalar(input_size.width, input_size.height), _cxy_delta_numx1_c2);
            Mat wh_delta = box_delta.colRange(new OpenCVRange(2, 4));
            Mat _wh_delta_numx1_c2 = wh_delta.reshape(2, wh_delta.rows());
            Core.divide(_wh_delta_numx1_c2, new Scalar(input_size.width, input_size.height), _wh_delta_numx1_c2);

            if (boxesMat == null)
                boxesMat = new Mat(num, 4, CvType.CV_32FC1);

            Mat xy1 = boxesMat.colRange(new OpenCVRange(0, 2));
            Mat xy2 = boxesMat.colRange(new OpenCVRange(2, 4));

            Core.divide(wh_delta, new Scalar(2.0), wh_delta);

            Core.subtract(cxy_delta, wh_delta, xy1);
            Core.add(xy1, anchors, xy1);

            Core.add(cxy_delta, wh_delta, xy2);
            Core.add(xy2, anchors, xy2);

            // NMS
            if (boxes_m_c4 == null)
                boxes_m_c4 = new Mat(num, 1, CvType.CV_64FC4);
            if (confidences_m == null)
                confidences_m = new Mat(num, 1, CvType.CV_32FC1);

            if (boxes == null)
                boxes = new MatOfRect2d(boxes_m_c4);
            if (confidences == null)
                confidences = new MatOfFloat(confidences_m);

            Mat boxes_m_c1 = boxes_m_c4.reshape(1, num);
            boxesMat.convertTo(boxes_m_c1, CvType.CV_64F);
            score.copyTo(confidences_m);
            MatOfInt indices = new MatOfInt();
            Dnn.NMSBoxes(boxes, confidences, score_threshold, nms_threshold, indices, 1f, topK);

            // get landmarks
            Mat results = new Mat(indices.rows(), 13, CvType.CV_32FC1);

            for (int i = 0; i < indices.total(); ++i)
            {
                int idx = (int)indices.get(i, 0)[0];

                float[] bbox_arr = new float[4];
                boxesMat.get(idx, 0, bbox_arr);
                results.put(i, 0, bbox_arr);

                float[] confidence_arr = new float[1];
                confidences.get(idx, 0, confidence_arr);
                results.put(i, 12, confidence_arr);

                float[] landmarks_arr = new float[8];
                landmark_delta.get(idx, 0, landmarks_arr);
                float[] anchors_arr = new float[2];
                anchors.get(idx, 0, anchors_arr);

                for (int j = 0; j < 8; ++j)
                {
                    if (j % 2 == 0)
                    {
                        landmarks_arr[j] /= (float)input_size.width;
                        landmarks_arr[j] += anchors_arr[0];
                    }
                    else
                    {
                        landmarks_arr[j] /= (float)input_size.height;
                        landmarks_arr[j] += anchors_arr[1];
                    }
                }

                results.put(i, 4, landmarks_arr);
            }

            indices.Dispose();

            // # TODO: still don't know the meaning of face bbox
            // # each landmark: hip center point; full body point; shoulder center point; upper body point;
            // #
            // # [
            // #   [face_bbox, landmarks, score]
            // #   ...
            // #   [face_bbox, landmarks, score]
            // # ]
            return results;
        }

        public virtual void visualize(Mat image, Mat results, bool print_results = false, bool isRGB = false)
        {
            if (image.IsDisposed)
                return;

            if (results.empty() || results.cols() < 13)
                return;

            StringBuilder sb = null;

            if (print_results)
                sb = new StringBuilder();

            for (int i = 0; i < results.rows(); ++i)
            {
                float[] score = new float[1];
                results.get(i, 12, score);
                float[] face_box = new float[4];
                results.get(i, 0, face_box);
                float[] person_landmarks = new float[8];
                results.get(i, 4, person_landmarks);

                float hip_point_x = person_landmarks[0];
                float hip_point_y = person_landmarks[1];
                float full_body_x = person_landmarks[2];
                float full_body_y = person_landmarks[3];
                float shoulder_point_x = person_landmarks[4];
                float shoulder_point_y = person_landmarks[5];
                float upper_body_x = person_landmarks[6];
                float upper_body_y = person_landmarks[7];

                // put score
                Imgproc.putText(image, String.Format("{0:0.0000}", score[0]), new Point(face_box[0], face_box[1] + 12), Imgproc.FONT_HERSHEY_DUPLEX, 0.5, new Scalar(0, 255, 0, 255));

                // draw box
                Imgproc.rectangle(image, new Point(face_box[0], face_box[1]), new Point(face_box[2], face_box[3]), new Scalar(0, 255, 0, 255), 2);

                // draw circle for full body
                // # radius = np.linalg.norm(hip_point - full_body).astype(np.int32)
                Mat full_body_vector = new Mat(1, 1, CvType.CV_32FC2, new Scalar(hip_point_x - full_body_x, hip_point_y - full_body_y));
                int radius = (int)Core.norm(full_body_vector);
                Imgproc.circle(image, new Point(hip_point_x, hip_point_y), radius, (isRGB) ? new Scalar(0, 0, 255, 255) : new Scalar(255, 0, 0, 255), 2);

                // draw circle for upper body
                // # radius = np.linalg.norm(shoulder_point - upper_body).astype(np.int32)
                Mat upper_body_vector = new Mat(1, 1, CvType.CV_32FC2, new Scalar(shoulder_point_x - upper_body_x, shoulder_point_y - upper_body_y));
                radius = (int)Core.norm(upper_body_vector);
                Imgproc.circle(image, new Point(shoulder_point_x, shoulder_point_y), radius, (isRGB) ? new Scalar(255, 255, 0, 255) : new Scalar(0, 255, 255, 255), 2);

                // draw points
                for (int j = 0; j < person_landmarks.Length; j += 2)
                {
                    Imgproc.circle(image, new Point(person_landmarks[j], person_landmarks[j + 1]), 2, (isRGB) ? new Scalar(255, 0, 0, 255) : new Scalar(0, 0, 255, 255), 2);
                }

                // Print results
                if (print_results)
                {
                    sb.AppendLine(String.Format("-----------person {0}-----------", i + 1));
                    sb.AppendLine(String.Format("score: {0:0.00}", score[0]));
                    sb.AppendLine(String.Format("face box: {0:0} {1:0} {2:0} {3:0}", face_box[0], face_box[1], face_box[2], face_box[3]));
                    sb.Append("person landmarks: ");
                    foreach (var p in person_landmarks)
                    {
                        sb.Append(String.Format("{0:0} ", p));
                    }
                    sb.AppendLine();
                }
            }

            if (print_results)
                Debug.Log(sb);
        }

        public virtual void dispose()
        {
            if (person_detection_net != null)
                person_detection_net.Dispose();

            if (input_sizeMat != null)
                input_sizeMat.Dispose();

            input_sizeMat = null;

            if (maxSizeImg != null)
                maxSizeImg.Dispose();

            maxSizeImg = null;

            if (boxesMat != null)
                boxesMat.Dispose();

            boxesMat = null;

            if (boxes_m_c4 != null)
                boxes_m_c4.Dispose();
            if (confidences_m != null)
                confidences_m.Dispose();
            if (boxes != null)
                boxes.Dispose();
            if (confidences != null)
                confidences.Dispose();

            boxes_m_c4 = null;
            confidences_m = null;
            boxes = null;
            confidences = null;
        }

        // OpenCV port of numpy.clip(a, a_min, a_max) in Python.
        // Restrict the range to [a_min, a_max].
        protected virtual void numpy_clip(Mat a, double a_min, double a_max)
        {
            if (a == null)
                throw new ArgumentNullException("a");
            if (a != null)
                a.ThrowIfDisposed();

            // Bring values within [0, a_max - a_min].
            Core.subtract(a, new Scalar(a_min), a);

            // Set negative values to 0.
            Imgproc.threshold(a, a, 0, 0, Imgproc.THRESH_TOZERO);

            // Set values above range to a_max - a_min.
            Imgproc.threshold(a, a, a_max - a_min, 0, Imgproc.THRESH_TRUNC);

            // Adjust the result to the desired range.
            Core.add(a, new Scalar(a_min), a);
        }

